# --- [ 2. وظيفة جدولة الأذكار التلقائية ] ---
# يتم استدعاؤها من ملف az_utils أو az_admin لضبط التوقيتات
def schedule_athkar(scheduler):
    """إضافة مهام الأذكار الصباحية والمسائية إلى المجدول الزمني"""
    
    # جدولة أذكار الصباح الساعة 7:00 صباحاً
    scheduler.add_job(
        lambda: asyncio.create_task(send_duas_batch(MORNING_DUAS, "dua_active", "أذكار الصباح")), 
        "cron", 
        hour=7, 
        minute=0
    )
    
    # جدولة أذكار المساء الساعة 8:00 مساءً (20:00)
    scheduler.add_job(
        lambda: asyncio.create_task(send_duas_batch(NIGHT_DUAS, "night_dua_active", "أذكار المساء")), 
        "cron", 
        hour=20, 
        minute=0
    )

